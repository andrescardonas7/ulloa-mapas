#!/usr/bin/env node
/**
 * Installs the local Git pre-commit activator without editing `core.hooksPath`.
 *
 * Writes `.git/hooks/pre-commit` so it delegates to the versioned
 * `.githooks/pre-commit`, which runs `.cursor/scripts/pre-commit-checks.mjs`
 * (includes the skill trust audit on staged `.cursor/skills/` changes).
 *
 * Usage (from repo root):
 *   node .cursor/scripts/install-git-hooks.mjs
 *   pnpm --dir .cursor hooks:install
 *
 * Idempotent: re-running refreshes the activator and the executable bits.
 */

import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..', '..');

const ACTIVATOR = `#!/bin/sh
# Auto-generated by .cursor/scripts/install-git-hooks.mjs
# Delegates to the versioned hook so checks stay in source control.
set -eu
repo_root="$(git rev-parse --show-toplevel 2>/dev/null || pwd)"
exec "$repo_root/.githooks/pre-commit" "$@"
`;

function resolveGitDir() {
  const result = spawnSync('git', ['rev-parse', '--git-dir'], {
    cwd: REPO_ROOT,
    encoding: 'utf8',
  });
  if (result.status !== 0) return null;
  const gitDir = result.stdout.trim();
  return path.isAbsolute(gitDir) ? gitDir : path.join(REPO_ROOT, gitDir);
}

function makeExecutable(file) {
  if (process.platform === 'win32') return;
  try {
    fs.chmodSync(file, 0o755);
  } catch {
    // Permission bits are best-effort (e.g. on filesystems that ignore them).
  }
}

function main() {
  const versionedHook = path.join(REPO_ROOT, '.githooks', 'pre-commit');
  if (!fs.existsSync(versionedHook)) {
    console.error(`Missing versioned hook: ${versionedHook}`);
    process.exit(1);
  }
  makeExecutable(versionedHook);

  const gitDir = resolveGitDir();
  if (!gitDir) {
    console.error('Not a git repository (could not resolve --git-dir).');
    process.exit(1);
  }

  const hooksDir = path.join(gitDir, 'hooks');
  fs.mkdirSync(hooksDir, { recursive: true });

  const activator = path.join(hooksDir, 'pre-commit');
  fs.writeFileSync(activator, ACTIVATOR, { mode: 0o755 });
  makeExecutable(activator);

  console.log(`Git pre-commit activator installed at ${activator}`);
  console.log('It delegates to .githooks/pre-commit (skill trust + staged checks).');
}

main();
